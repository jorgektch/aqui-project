# Función para graficar puntos de un color
def graficar_puntos(grafico, puntos, color):
    x, y = zip(*puntos)
    grafico.scatter(x, y, c=color, s=100, marker='s')

# Crear ventana Tkinter
ventana = tk.Tk()
ventana.title("Para Jorge Jesus")

figura = Figure(figsize=(8, 8))  # Para aumentar el tamaño de la figura
grafico = figura.add_subplot(111)


# Para ajustar los límites de los ejes para que cubran toda la imagen
grafico.set_xlim(-5, 20)  # Límite en el eje x
grafico.set_ylim(-5, 25)  # Límite en el eje y
grafico.set_aspect('equal')
grafico.axis('off')  # Oculta los ejes

# Para que aparezca cada punto de colores
graficar_puntos(grafico, negro, 'black')
graficar_puntos(grafico, rojo, 'red')
graficar_puntos(grafico, amarillo, 'yellow')

canvas = FigureCanvasTkAgg(figura, master=ventana)
canvas.draw()
